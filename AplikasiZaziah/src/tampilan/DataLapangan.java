    private void btnubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnubahActionPerformed
        // TODO add your handling code here:
        String idlapangan = idkon.getText();
        String nolapangan = nokon.getText();
        String Tipe =(String) tipe.getSelectedItem();
        String harga = harg.getText();
 
        
        if(idlapangan.isEmpty()) 
        { 
            JOptionPane.showMessageDialog(null, "Masukkan ID Lapangan");
        }
        else if(nolapangan.isEmpty()) 
        { 
            JOptionPane.showMessageDialog(null, "Masukkan No Lapangan");
        }
        else if(Tipe.isEmpty()) 
        { 
            JOptionPane.showMessageDialog(null, "Masukkan Tipe");
        }
        else if(harga.isEmpty()) 
        { 
            JOptionPane.showMessageDialog(null, "Masukkan Harga");
        
        }else {
            try {
               String sql = "update datalapangan set no_lapangan=?, tipe_lapangan=?, harga=? where id_lapangan=?";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql); 
            pst.setString(1, nolapangan);
            pst.setString(2, Tipe);
            pst.setString(3, harga);
            pst.setString(4, idlapangan);
            
                pst.execute();
                JOptionPane.showMessageDialog(null, "Data Berhasil Di Ubah");
                clear();
                listed();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data Gagal Di Ubah"+ e);
            }
        }
    }//GEN-LAST:event_btnubahActionPerformed

