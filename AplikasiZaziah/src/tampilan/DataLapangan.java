    public void id_auto(){
        try{
            Connection c = new koneksi().connect();
            Statement stat = c.createStatement();
            String sql = "select max(right(id_lapangan,4)) as no from datalapangan";
            ResultSet rs = stat.executeQuery(sql);
            idkon.setEnabled(false);
            while (rs.next()){
                if(rs.first()==false){
                    idkon.setText("IDP0001");
                }else {
                    rs.last();
                    int set_kode = rs.getInt(1)+1;
                    String no = String.valueOf(set_kode);
                    int kode_next = no.length();
                    for (int a = 0 ; a<4 - kode_next; a++){
                        no = "0"+no;
                        
                }
                    idkon.setText("IDP"+no);
                }
            }
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
